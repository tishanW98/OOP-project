    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        User authenticatedUser = LittleBagShop.authenticate(username, password);

        if (authenticatedUser != null) {
            this.setVisible(false); // Close the login form
            if (authenticatedUser instanceof Manager) {
                // Open Manager UI
                Managerc managerUI = new Managerc();
                managerUI.setVisible(true);
            } else if (authenticatedUser instanceof Cashier) {
                // Open Cashier UI
                CashierUI cashierUI = new CashierUI();
                cashierUI.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid credentials", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

